diaSemana = int(input("Digite 1- Domingo, 2- Segunda, 3- Terça, 4- Quarta, 5- Quinta, 6- Sexta, 7- Sábado: "))

if diaSemana == 1:
    print("Domingo")

elif diaSemana == 2:
    print("Segunda")

elif diaSemana == 3:
    print("Terça")

elif diaSemana == 4:
    print("Quarta")

elif diaSemana == 5:
    print("Quinta")

elif diaSemana == 6:
    print("Sexta")

elif diaSemana == 7:
    print("Sábado")

else:
    print("Número inválido")
